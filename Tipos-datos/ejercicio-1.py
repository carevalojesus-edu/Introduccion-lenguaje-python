entero = 10
flotante = 9.5
cadena = "Hola a todos"
booleano = False

# Operaciones basicas
suma = entero + flotante # Resultado 15.5
print("Resultado de la suma:", suma)

multplicacion = entero * flotante # Resultado 95
print("Resultado de la multiplicacion:", multplicacion)

# Concatenacion de cadenas de texto
nueva_cadena = cadena + ", ¿cómo estás?"
print(nueva_cadena)

# Uso de booleano en un sentencia if

if booleano:
    print("El valor booleano es verdadero") # Esto se va a imprimir
else:
    print("El valor booleano es falso")