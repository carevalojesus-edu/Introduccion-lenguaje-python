# Convertir un numero a una cadena de texto y combinarlo con otra cadena
# Convertit una cadena de texto que represente en un entero

# Convertir de entero a flotante (float)
entero = 112
flotante = float(entero)
print("Convertido a flotante", flotante)

# Convertir de flotante a entero (int)
flotante = 80.9
entero = int(flotante)
print("Convertido a entero", entero)

# Convertir de numero a una cadena (str)
# (type) para saber que tipo de datos es

numero = 123
print(type(numero))
cadena = str(numero)
print("Numero convertido a cadena", cadena)
print(type(cadena))

