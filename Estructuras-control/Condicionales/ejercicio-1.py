# Ejercicio de clasificacion de numeros

# Declarar un variable entero
# vamos a usar if. elif, else para clasificar como positivo, negativo o cero
# imprimir un mensaje de acuerdo a su clasificacion

entero = 0

if entero > 0:
    print("EL numero es positivo")
elif entero < 0:
    print("El numero es negativo")
else:
    print("El numero es cero")